# Take two numbers as input from user and store the values in variables number_1 & number_2
# Hint: Use input() function


number_1 = input("Enter the first number: ")
number_2 = input("Enter the second number: ")

# Perform below arithmetic operations on those two numbers using operators and print the corresponding output
# Addition


sum=number_1+number_2
print("The sum of", number_1, "and", number_2, "is",sum)

# Subtraction
print("The difference of", number_1, "and", number_2, "is", number_1 - number_2)

# Division
print("The quotient of", number_1, "and", number_2, "is", number_1 / number_2)

# Modulo Division
print("The remainder of", number_1, "and", number_2, "is", number_1 % number_2)

# Floor Division
print("The floor quotient of", number_1, "and", number_2, "is", number_1 // number_2)

# Multiplication
print("The product of", number_1, "and", number_2, "is", number_1 * number_2)

# Exponentiation
print("The power of", number_1, "to the", number_2, "is", number_1 ** number_2)
